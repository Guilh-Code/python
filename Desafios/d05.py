# Faça um programa que leia um número inteiro e mostre na tela o seu sucessor e seu antecessor


# Pede ao usuário para digitar um valor e armazena como um número inteiro
v = int(input('Digite um valor: '))

# Exibe o valor do antecessor (v - 1) e do sucessor (v + 1)
print(f'O valor Antecessor é {v-1} e o sucessor é {v+1}')

