# Crie um programa que leia dois números e mostre a soma entre elas.


# Pede ao usuário para digitar o primeiro número e armazena como um número inteiro
n1 = int(input('Primeiro número '))

# Pede ao usuário para digitar o segundo número e armazena como um número inteiro
n2 = int(input('Segundo número '))

# Calcula a soma dos dois números
soma = n1 + n2

# Exibe o resultado da soma
print(f'A soma é {soma}')
